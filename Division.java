public class Division 
{
    public static float calDivision(float num1, float num2)
    {
        return num1/num2;
    }
    public static void main(String[] args) 
    {
        float a = 12;
        float b = 6;

        float div = calDivision(a, b);
        System.out.println(div);
    }    
}
